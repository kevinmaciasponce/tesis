package com.multiplos.cuentas.pojo.amortizacion;

public class SimuladorPrincipalResponse {

	private String tipo;
	private String totalRecibir;
	private String interesGanado;

	public SimuladorPrincipalResponse() {
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getTotalRecibir() {
		return totalRecibir;
	}

	public void setTotalRecibir(String totalRecibir) {
		this.totalRecibir = totalRecibir;
	}

	public String getInteresGanado() {
		return interesGanado;
	}

	public void setInteresGanado(String interesGanado) {
		this.interesGanado = interesGanado;
	}

	

}
